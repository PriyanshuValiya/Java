import java.util.*;

/* For Loop */

public class Loops {
    public static void main(String[] args) {
       for(int i = 0; i < 5; i++) {
          System.out.println("Loops World");
       }
    }
 }
 
 /* While Loop */
 
 public class Loops {
    public static void main(String[] args) {
       int i = 1;
       while(i <= 10) {
          System.out.println(i);
          i++;
       }
    }
 }
 
 /* Do-While Loop */
 
 public class Loops {
    public static void main(String[] args) {
       int i = 1;
       do {
          System.out.println(i);
          i++;
       } while (i <= 10);
    }
 }